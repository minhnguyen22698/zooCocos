cc.Class({
    extends: cc.Component,
    properties: {
        _objname: 'not set',
        objname: {
            get: function () {
                return this._objname;
            },
            set: function (val) {
                this._objname = val
            }
        }
    },
    talk(name){
        this.objname=name
        cc.log("Hello, I`m "+this.objname)
    }
});
